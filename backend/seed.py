async def main() -> None:
    async with async_session() as db:
        # 1. Clear existing data
        await db.execute(delete(News))
        await db.execute(delete(Category))
        await db.flush()
        print("Cleared existing news and categories.")

        # 2. Get admin user
        result = await db.execute(select(User).where(User.email == "admin@gmail.com"))
        admin = result.scalar_one()
        print(f"Admin found: {admin.email}")

        # 3. Create categories
        categories: dict[str, object] = {}
        for cat_data in CATEGORIES:
            cat = await create_category(db, CategoryCreate(**cat_data))
            categories[cat.slug] = cat
            print(f"  Category: {cat.name}")

        # 4. Create news
        for news_data in NEWS:
            cat_slug = news_data.pop("category_slug")
            blocks = news_data.pop("blocks")
            news_in = NewsCreate(
                **news_data,
                category_ids=[categories[cat_slug].id],
                blocks=blocks,
            )
            news = await create_news(db, news_in, admin.id)
            print(f"  News: {news.title[:65]}...")

        await db.commit()
        print("\nDone! 3 categories and 6 news articles created.")


if __name__ == "__main__":
    asyncio.run(main())
